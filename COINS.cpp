long long int calcTotal(long long int n) 
{
  if (n == 0) 
  	return 0; 
  long long int aux = dp[n];
 
  if (aux == 0) {
    aux = maX( n , calcTotal(n / 2) + calcTotal(n / 3) + calcTotal(n / 4) );
    dp[n] = aux;
  }
  return aux;
}

